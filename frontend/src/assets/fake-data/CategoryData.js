const getCards_random = (count) => {
  const max = card_data_category.length - count;
  const min = 0;
  const start = Math.floor(Math.random() * (max - min) + min);
  return card_data_category.slice(start, start + count);
};
const getCards = (index, count) => {
  if (count > card_data_category.length - count - index) {
    return getCards_random(count);
  } else {
    return card_data_category.slice(index, index + count);
  }
};

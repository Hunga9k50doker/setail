const getSortCards = getCards_random(10).sort(function (a, b) {
  return Number(a.cost) - Number(b.cost);
});

// sort high to low
const getSortParseCards = getCards_random(10).sort(function (a, b) {
  return Number(b.cost) - Number(a.cost);
});

